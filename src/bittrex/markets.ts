export const references: Array<string> = ['BTC', 'ETH', 'USDT'];
export const chosenSymbols: Array<string> = references.concat([
  symbols.Ripple,
  symbols.Litecoin,
  symbols.NEM,
  symbols.Dash,
  symbols.Monero,
  symbols.EthereumClassic,
  symbols.Waves,
]);

const isReference = symbol => references.indexOf(symbol) > -1;
const isChosen = symbol => chosenSymbols.indexOf(symbol) > -1;

export const chosenMarkets: Array<string> = all.filter(market => {
  const [from, to] = market.split(/-/);

  if (!isChosen(from) || !isChosen(to)) {
    return false;
  }

  return true;
});

